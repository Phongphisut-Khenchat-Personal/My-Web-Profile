function getProjectTag(project) {
  return project.tag;
}

function getProjectDesc(project) {
  return currentLang === "th" ? project.descTh : project.descEn;
}

function showImageFallback(container, project) {
  container.innerHTML = "";
  container.classList.add("project-image--fallback", `project-image--accent-${project.accent || "slate"}`);
  const icon = LANG_ICONS[project.lang] || "fas fa-folder-open";
  container.innerHTML = `<i class="${icon}" aria-hidden="true"></i><span>${project.lang || "Project"}</span>`;
}

function buildProjectImage(project) {
  const imageWrap = document.createElement("div");
  imageWrap.className = "project-image";

  const tryImage = (src) => {
    const img = document.createElement("img");
    img.src = src;
    img.alt = project.title;
    img.loading = "lazy";
    img.decoding = "async";
    img.onerror = () => showImageFallback(imageWrap, project);
    imageWrap.appendChild(img);
  };

  if (project.image) {
    tryImage(project.image);
  } else if (project.repo) {
    tryImage(`https://opengraph.githubassets.com/1/Phongphisut-Khenchat-Personal/${project.repo}`);
  } else {
    showImageFallback(imageWrap, project);
  }

  return imageWrap;
}

function buildProjectCard(project, index, extraClass = "") {
  const card = document.createElement("article");
  card.className = `project-card${extraClass ? ` ${extraClass}` : ""}`;
  card.dataset.projectId = project.id;

  card.appendChild(buildProjectImage(project));

  const body = document.createElement("div");
  body.className = "project-body";
  body.innerHTML = `
    <span class="project-tag">${getProjectTag(project)}</span>
    <h3>${project.title}</h3>
    <span class="project-lang">${project.lang}</span>
    <p class="project-desc">${getProjectDesc(project)}</p>
    <div class="project-links"></div>
  `;

  const links = body.querySelector(".project-links");
  if (project.github) {
    links.innerHTML += `<a href="${project.github}" class="project-link" target="_blank" rel="noopener noreferrer"><i class="fab fa-github" aria-hidden="true"></i> GitHub</a>`;
  }
  if (project.demo) {
    links.innerHTML += `<a href="${project.demo}" class="project-link" target="_blank" rel="noopener noreferrer"><i class="fas fa-external-link-alt" aria-hidden="true"></i> Demo</a>`;
  }

  card.appendChild(body);
  card.setAttribute("aria-label", `${project.title} — slide ${index + 1}`);
  return card;
}

function buildFeaturedCard(project) {
  const card = buildProjectCard(project, 0, "featured-card");
  const body = card.querySelector(".project-body");
  const langEl = body.querySelector(".project-lang");
  if (langEl) langEl.remove();

  if (project.stack?.length) {
    const stackWrap = document.createElement("div");
    stackWrap.className = "project-stack";
    stackWrap.innerHTML = project.stack.map((item) => `<span>${item}</span>`).join("");
    body.insertBefore(stackWrap, body.querySelector(".project-desc"));
  }

  return card;
}

function renderFeaturedCarousel() {
  const track = document.getElementById("featured-track");
  if (!track) return;
  track.innerHTML = "";

  for (let set = 0; set < 2; set += 1) {
    featuredProjects.forEach((project) => {
      const card = buildFeaturedCard(project);
      if (set === 1) card.setAttribute("aria-hidden", "true");
      track.appendChild(card);
    });
  }
}

function initInfiniteCarousel({ viewport, track, prevBtn, nextBtn, speed = 0.45 }) {
  let position = 0;
  let halfWidth = 0;
  let paused = false;
  let rafId = null;

  function measure() {
    halfWidth = track.scrollWidth / 2;
  }

  function getStep() {
    const card = track.querySelector(".project-card");
    if (!card) return 340;
    const gap = parseFloat(getComputedStyle(track).gap) || 20;
    return card.offsetWidth + gap;
  }

  function wrapPosition() {
    if (halfWidth <= 0) return;
    while (position <= -halfWidth) position += halfWidth;
    while (position > 0) position -= halfWidth;
  }

  function applyTransform() {
    wrapPosition();
    track.style.transform = `translateX(${position}px)`;
  }

  function tick() {
    if (!paused && !window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
      position -= speed;
      applyTransform();
    }
    rafId = requestAnimationFrame(tick);
  }

  function nudge(direction) {
    position += direction * getStep();
    applyTransform();
  }

  prevBtn?.addEventListener("click", () => nudge(1));
  nextBtn?.addEventListener("click", () => nudge(-1));

  viewport?.addEventListener("mouseenter", () => {
    paused = true;
  });
  viewport?.addEventListener("mouseleave", () => {
    paused = false;
  });
  viewport?.addEventListener("touchstart", () => {
    paused = true;
  }, { passive: true });
  viewport?.addEventListener("touchend", () => {
    paused = false;
  }, { passive: true });

  window.addEventListener("resize", measure);

  measure();
  if (rafId) cancelAnimationFrame(rafId);
  rafId = requestAnimationFrame(tick);

  return { remeasure: measure };
}

function updateProjectDescriptions() {
  document.querySelectorAll(".project-card, .featured-card").forEach((card) => {
    const project = allProjects.find((p) => p.id === card.dataset.projectId);
    if (!project) return;
    const tagEl = card.querySelector(".project-tag");
    const descEl = card.querySelector(".project-desc");
    if (tagEl) tagEl.textContent = getProjectTag(project);
    if (descEl) descEl.textContent = getProjectDesc(project);
  });
}

